-- 5. Employee Onboarding
CREATE TABLE IF NOT EXISTS employee_onboarding (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "employeeName" TEXT,
    dob DATE,
    nationality TEXT,
    "countryCode" TEXT,
    "mobileNo" TEXT,
