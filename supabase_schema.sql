-- 10xWorkforce.AI Master Data & Module Tables (All Time/Date standardized)

-- ==========================================
-- MASTER DATA TABLES (Required for AI Agents)
-- ==========================================

-- M1. Sites (Projects) Master
CREATE TABLE IF NOT EXISTS sites (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    site_name TEXT UNIQUE,
    site_code TEXT,
    location TEXT,
    total_budget NUMERIC DEFAULT 0,
    required_manpower NUMERIC DEFAULT 0,
    status TEXT DEFAULT 'Active'
);

-- M2. Employees Master
CREATE TABLE IF NOT EXISTS master_employees (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id TEXT UNIQUE,
    employee_name TEXT,
    trade TEXT,
    labor_type TEXT,
    base_salary NUMERIC,
    status TEXT DEFAULT 'Active'
);

-- M3. Materials Master
CREATE TABLE IF NOT EXISTS master_materials (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    material_code TEXT UNIQUE,
    material_name TEXT,
    category TEXT,
    standard_price NUMERIC,
    approved_vendor TEXT
);

-- M4. Tools Master
CREATE TABLE IF NOT EXISTS master_tools (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tag_name TEXT UNIQUE,
    tool_name TEXT,
    brand TEXT,
    total_owned NUMERIC,
    warranty_expiry DATE
);

-- ==========================================
-- TRANSACTION / MODULE TABLES
-- ==========================================

-- 1. Daily Manpower
CREATE TABLE IF NOT EXISTS daily_manpower (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "logType" TEXT,
    "siteNo" TEXT,
    "siteName" TEXT,
    location TEXT,
    "startTime" TIME,
    "endTime" TIME,
    engineer TEXT,
    foreman TEXT,
    driver TEXT,
    "otherStaff" TEXT,
    "otherStaffTrade" TEXT,
    "taskTitle" TEXT,
    remarks TEXT,
    date DATE DEFAULT CURRENT_DATE,
    "agent_status" TEXT DEFAULT 'pending',
    "agent_metadata" JSONB
);

-- 2. Camp Boss
CREATE TABLE IF NOT EXISTS camp_boss (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "employeeId" TEXT,
    "employeeName" TEXT,
    "campLocation" TEXT,
    "roomNumber" TEXT,
    status TEXT,
    remarks TEXT,
    date DATE DEFAULT CURRENT_DATE,
    "agent_status" TEXT DEFAULT 'pending',
    "agent_metadata" JSONB
);

-- 3. Petty Cash
CREATE TABLE IF NOT EXISTS petty_cash (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "pettyCashHolder" TEXT,
    "supplierName" TEXT,
    description TEXT,
    "projectCode" TEXT,
    "projectName" TEXT,
    currency TEXT,
    amount NUMERIC,
    vat NUMERIC,
    "totalAmount" NUMERIC,
    "invoiceUrisJson" JSONB,
    date DATE DEFAULT CURRENT_DATE,
    "agent_status" TEXT DEFAULT 'pending',
    "agent_metadata" JSONB
);

-- 4. MR Procurement
CREATE TABLE IF NOT EXISTS mr_procurement (
